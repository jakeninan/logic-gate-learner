       <center> <h2 style="text-decoration:underline;color:red;">LOGIC GATES</h2></center>
       <h2>DESCRIPTION:- A logic gate is an arrangement of control switches used to calculate
               operation using boolean logic in digital circuits.They are primarly
               implemented electronically but can also be constructed using electromagnetic
               relays.</h2>
       <h2>Three types of essential logic gates are AND,OR,NOT.For convinience derived types
           are NAND,NOR,XOR and XNOR.</h2>
     <ul>
         <li><h2>AND GATE:-  The AND gate is an electronic circuit that gives a high output (1) only if all its inputs are high.  A dot (.) is used to show the AND operation i.e. A.B.  Bear in mind that this dot is sometimes omitted i.e. AB        
       </h2>
         </li>     
         <li> <h2>OR GATE:The OR gate is an electronic circuit that gives a high output (1) if one or more of its inputs are high.  A plus (+) is used to show the OR operation.-          
       </h2>
         </li>    
         <li><h2>NOT GATE:- The NOT gate is an electronic circuit that produces an inverted version of the input at its output.  It is also known as an inverter.  If the input variable is A, the inverted output is known as NOT A.  This is also shown as A', or A with a bar over the top, as shown at the outputs.       
       </h2>
         </li> 
         <li> <h2>NAND GATE:-This is a NOT-AND gate which is equal to an AND gate followed by a NOT gate.  The outputs of all NAND gates are high if any of the inputs are low. The symbol is an AND gate with a small circle on the output.           
       </h2>
         </li>
         <li> <h2>NOR GATE:- This is a NOT-OR gate which is equal to an OR gate followed by a NOT gate.  The outputs of all NOR gates are low if any of the inputs are high.         
       </h2>
         </li>
         <li><h2>EXOR GATE:-The 'Exclusive-OR' gate is a circuit which will give a high output if either, but not both, of its two inputs are high.  An encircled plus sign () is used to show the EOR operation.        
       </h2>
         </li>
         <li><h2>EXNOR GATE:- The 'Exclusive-NOR' gate circuit does the opposite to the EOR gate. It will give a low output if either, but not both, of its two inputs are high.         
       </h2>
         </li>
     </ul>  </table>
